                        L’Association des médecins des Résidents de Casablanca « ARC » est une association apolitique, à but non lucratif, ayant pour objectif la défense des intérêts moraux, matériels et professionnels de ses 900 membres.
Créée en 1997, l’Association des médecins Résidents de Casablanca est devenue une des associations les plus actives dans le domaine de la santé au niveau régional et national.<br>
L’ARC regroupe l’ensemble des médecins résidents admis par concours de spécialité au sein du CHU Ibn Rochd, ce dernier qui est considéré comme le CHU numéro 1 au Maroc par rapport au nombre de services et surtout par la richesse de l’intervention dans le secteur de santé qu’il fournit sur le plan provincial, régional et même national.<br>
Notre association regroupe environ 900 médecins résidents depuis la première année jusqu’à l’obtention du diplôme de spécialité en médecine, chirurgie ou biologie.
